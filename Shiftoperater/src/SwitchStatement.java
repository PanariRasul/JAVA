public class SwitchStatement {
    public static void main(String[] args) {
        int num=2;

        switch (num){
            case 0:
                System.out.println("Number is Zero");
                break;
            case 1:
                System.out.println("Number Is One");
                break;
            case 2:
                System.out.println("Number Is Two");
                break;
            case 3:
                System.out.println("Number is Three");
                break;
        }
    }
}
