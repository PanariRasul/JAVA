public class WhileLoop {
    public static void main(String[] args) {

        int i=1;

        while (i<=10)
        {
            System.out.println(i+" Hello World!");
            i++;
        }
        System.out.println("==========================================================================");

        int j=25;
        do{
            System.out.println(j+" Hello Motrola");
            j++;
        }while (j<=15);
    }


}
